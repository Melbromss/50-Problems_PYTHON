s = input()

if s == 'T':
    base = int(input())
    height = int(input())
    res = 1/2 * base * height
    print("Area of Triagnle is :",res)
    
elif s == 'R':
    width = int(input())
    length = int(input())
    res = width + length
    print("Area of Rantangle is :",res)
    
    